function main() {
    let rows = input.split('\n').map((v) => v.split('').reverse()).reverse();
    const patToIdxMap = {};
    const idxToPatMap = {};
    for (let i = 0; i < 1000000000; i++) {
        const pat = rows.map((r) => r.join('')).join('\n');
        if (pat in patToIdxMap) {
            const first = patToIdxMap[pat];
            const cycle = i - first;
            const rem = (1000000000 - first) % cycle;
            rows = idxToPatMap[first + rem].split('\n').map((r) => r.split(''));
            break;
        }
        patToIdxMap[pat] = i;
        idxToPatMap[i] = pat;
        rows = processInput(rows);
        rows = processInput(rows);
        rows = processInput(rows);
        rows = processInput(rows);
    }
    return rows.map((r, i) => r.filter((v) => v === 'O').length * (i + 1)).reduce((p, c) => p + c, 0);
}

function rotate(input) {
    const targetLen = input[0].length;
    const result = [];
    for (let i = 0; i < targetLen; i++) {
        result.push(input.map((v) => v[i]).reverse());
    }
    return result;
}

function processInput(input) {
    return rotate(input).map((original) => {
        const final = [];
        let last = -1;
        let count = 0;
        for (let i = 0; i < original.length; i++) {
            switch (original[i]) {
                case 'O':
                    count++;
                    break;
                case '#':
                    for (let i = 0; i < count; i++) {
                        final.push('O');
                    }
                    for (let j = 0; j < i - last - count - 1; j++) {
                        final.push('.');
                    }
                    count = 0;
                    last = i;
                    final.push('#');
                    break;
            }
        }
        for (let i = 0; i < count; i++) {
            final.push('O');
        }
        for (let j = 0; j < original.length - last - count - 1; j++) {
            final.push('.');
        }
        return final;
    });
}

